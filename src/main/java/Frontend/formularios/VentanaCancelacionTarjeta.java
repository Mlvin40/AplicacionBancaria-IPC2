    private void btnCancelarTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarTarjetaActionPerformed
        //AQUI PARA CANCELAR
        try {
            if (validarYObtenerCampos()) {
                TarjetaDB tarjetaDB = new TarjetaDB();
                tarjetaDB.cancelarTarjeta(numeroTarjeta);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Revisar los datos ingresados");
        }

    }//GEN-LAST:event_btnCancelarTarjetaActionPerformed

    private boolean validarYObtenerCampos() {
        numeroTarjeta = jTextField1.getText();
        if (numeroTarjeta.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el n√∫mero de tarjeta");
            return false;
        }
        return true;
    }

