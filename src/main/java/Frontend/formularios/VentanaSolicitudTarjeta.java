    private void obtenerDatos() {
        numeroSolicitud = txtNumeroSolicitud.getText();
        fecha = txtFecha.getText();
        tipo = txtTipoTarjeta.getSelectedItem().toString();
        nombreSolicitante = txtNombreSolicitante.getText();
        salario = Double.parseDouble(txtSalario.getText());
        direccion = txtDireccion.getText();
    }

    // Este metodo verifica si algun campo esta vacio
    private boolean campoVacio() {
        return txtNumeroSolicitud.getText().isEmpty() || txtFecha.getText().isEmpty() || txtNombreSolicitante.getText().isEmpty() || txtSalario.getText().isEmpty() || txtDireccion.getText().isEmpty();
    }
