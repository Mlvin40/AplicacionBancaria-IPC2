    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            if (campoVacio()) {
                JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos");
            } else {
                obtenerDatos();
                Solicitud solicitud = new Solicitud(numeroSolicitud, fecha, TipoTarjeta.valueOf(tipo), nombreSolicitante, salario, direccion);

                SolicitudDB solicitudDB = new SolicitudDB();
                solicitudDB.crearSolicitud(solicitud);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Revise que los campos sean validos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

